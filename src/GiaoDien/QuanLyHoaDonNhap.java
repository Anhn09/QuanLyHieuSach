    public void layHDN() {
      String filePath = "D:\\hoadon.txt";
    ds.clear(); // Xóa danh sách hiện tại trước khi lấy dữ liệu mới
    
    try {
        File file = new File(filePath);
        Scanner scanner = new Scanner(file);

        while (scanner.hasNextLine()) {
            String line = scanner.nextLine();
            String[] fields = line.split(",");

            if (fields.length == 7) {
                String maHDN = fields[0];
                String maSach = fields[1];
                String maNV = fields[2];
                String ngayNhap = fields[3];
                int soLuongN = Integer.parseInt(fields[4]);
                float donGiaN = Float.parseFloat(fields[5]);
                float thanhTienN = Float.parseFloat(fields[6]);

                HoaDonNhap hd = new HoaDonNhap(maHDN, maSach, maNV, ngayNhap, soLuongN, donGiaN, thanhTienN);
                ds.add(hd);
            } 
        }

        scanner.close();
    } catch (FileNotFoundException e) {
        e.printStackTrace();
    }
    }

