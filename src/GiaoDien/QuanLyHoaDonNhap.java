private boolean kiemTraMaHoaDonTonTai(String maHD) {
    for (HoaDonNhap hd : ds) {
        if (hd.getMaHDN().equals(maHD)) {
            return true; // Mã hóa đơn đã tồn tại trong danh sách
        }
    }
    
    String filePath = "D:\\hoadon.txt";
    File file = new File(filePath);
    if (file.exists()) {
        try {
            Scanner scanner = new Scanner(file);
            while (scanner.hasNextLine()) {
                String line = scanner.nextLine();
                String[] fields = line.split(",");
                if (fields.length >= 1 && fields[0].equals(maHD)) {
                    scanner.close();
                    return true; // Mã hóa đơn đã tồn tại trong tệp tin
                }
            }
            scanner.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(QuanLyHoaDonNhap.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    return false; // Mã hóa đơn không tồn tại
}
